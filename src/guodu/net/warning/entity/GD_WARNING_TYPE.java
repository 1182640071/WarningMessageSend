package guodu.net.warning.entity;

public class GD_WARNING_TYPE {
	private String warning_id;
	private String type;
	private String wx_user;
	private String mes_user;
	
	public String getWarning_id() {
		return warning_id;
	}
	public void setWarning_id(String warning_id) {
		this.warning_id = warning_id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getWx_user() {
		return wx_user;
	}
	public void setWx_user(String wx_user) {
		this.wx_user = wx_user;
	}
	public String getMes_user() {
		return mes_user;
	}
	public void setMes_user(String mes_user) {
		this.mes_user = mes_user;
	}
}
